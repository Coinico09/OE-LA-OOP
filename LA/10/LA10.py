class vehicle():
    def __init__(self, brand, model, fuelType):
        self.brand = brand
        self.model = model
        self.fuelType = fuelType
        
    def describevehicle(self):
        print(f"{self.brand}, {self.model}, {self.fuelType}")
        
class vehicle(vehicle):
    pass

toyota = vehicle("toyota", "86", "premium unleaded fuel")
toyota.describevehicle()

